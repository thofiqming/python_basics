# dynamically bound data type
value = 10
str_value = "hello"
list_values = [10, 20, 30, 40]
set_values = {10, 20, 30, 30, 40}

# formatting and printing the value
print(f" type : {type(value)}, value: {value} ")
print(f" type : {type(str_value)}, value: {str_value} ")
print(f" type : {type(list_values)}, value: {list_values} ")
print(f" type : {type(set_values)}, value: {set_values} ")

